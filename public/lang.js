let currentLang = 'en';

function applyTranslations() {
    document.querySelectorAll("[data-key]").forEach(el => {
        const key = el.getAttribute("data-key");
        if (translations[key]) {
            // Use innerHTML to preserve potential formatting if any (though these are mostly text)
            // or innerText. Used innerText previously, stuck with it or logic from user check.
            // User script used innerText.
            el.innerText = translations[key][currentLang];
        }
    });

    // Ensure font class is synced
    const body = document.body;
    if (currentLang === 'ml') {
        body.classList.add('ml');
    } else {
        body.classList.remove('ml');
    }
}

function toggleLanguage() {
    const body = document.body;
    const btn = document.getElementById("langToggle");

    // Fade out
    body.classList.add("fade-out");

    setTimeout(() => {
        // Switch State
        currentLang = currentLang === 'en' ? 'ml' : 'en';
        applyTranslations();

        // Update Button Text using the dynamic button text key
        if (btn) {
            btn.innerText = translations.lang_button_to_ml[currentLang];
        }

        // Fade in
        body.classList.remove("fade-out");
        body.classList.add("fade-in");

        // Clean up animation class
        setTimeout(() => body.classList.remove("fade-in"), 600);

        // Persist
        localStorage.setItem('language', currentLang);
    }, 600);
}

function initLang() {
    const saved = localStorage.getItem('language');
    if (saved) {
        currentLang = saved;
    }
    applyTranslations();

    // Update button initial state
    const btn = document.getElementById("langToggle");
    if (btn) {
        btn.innerText = translations.lang_button_to_ml[currentLang];
    }
}

// Attach to window
window.initLang = initLang;
window.toggleLanguage = toggleLanguage;

// Auto-run
document.addEventListener('DOMContentLoaded', initLang);
